namespace DesafioPratico01_Rocketseat;

public class ValoresDetalhados
{
    public static double CalculoSoma(double SalarioAnual, double SalarioMensal) => SalarioAnual + SalarioMensal;

    public static double CalculoSubtracao(double SalarioAnual, double SalarioMensal) => SalarioAnual - SalarioMensal;

    public static double CalculoMultiplicacao(double SalarioAnual, double SalarioMensal) => SalarioAnual * SalarioMensal;

    public static double CalculoDivisao(double SalarioAnual, double SalarioMensal) => SalarioAnual / SalarioMensal;

    public static double CalculoMedia(double SalarioAnual, double SalarioMensal) => (SalarioAnual + SalarioMensal) / 2;
}
